<h4 style="color:#666666">... Redefining RSS</h4>
<p style="text-align: left;">Read Some Stuff is a new way to connect with the world's information through the use of RSS (Real Simple Syndication) feeds. "Redefining RSS" is what you do through the use of advanced tools under your control.</p>
<p style="text-align: left;">Read Some Stuff is built to allow more flexibility and control over the information retrieved through your feeds. It puts the control back in your hands allowing you to define what is and is not important to you. Your time is more valuable then ever. Read Some Stuff is here to allow you to have all the world's information you are interested in at your fingertips and what your not interested in far out of your way.</p>
<p style="text-align: left;">Not sure what a RSS feed is or how to work with them? Not a problem. By using your own search terms you can generate your own feeds from sites such as Google News, YouTube, Twitter, and more. You can also search feeds that have been predefined. As you build your comfort level with RSS feeds, you may start incorporating feeds that you have found into your stream.</p>
